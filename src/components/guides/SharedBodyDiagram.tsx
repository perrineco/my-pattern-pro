// ─── Composant principal (usage isolé par catégorie) ─────────────────────────

interface SharedBodyDiagramProps {
  category: Category;
  /** Rendu de l'overlay de mesures. Reçoit les positions du corps. */
  renderOverlay?: (pos: BodyPositions) => ReactNode;
  /** Hauteur du viewBox (optionnel, calculée automatiquement sinon). */
  viewBoxHeight?: number;
  className?: string;
}

export function SharedBodyDiagram({ category, renderOverlay, viewBoxHeight, className }: SharedBodyDiagramProps) {
  const pos = getPositions(category);
  const vbHeight = viewBoxHeight ?? (category === "kids" ? 345 : 340);

  // Scale down the kids silhouette so it appears smaller & more proportional
  const kidsScale = 0.75;
  const kidsTranslateX = (240 * (1 - kidsScale)) / 2; // center horizontally
  const kidsTranslateY = 10; // small top offset

  const WomenScale = 0.75;
  const WomenTranslateX = (240 * (1 - WomenScale)) / 2; // center horizontally
  const WomenTranslateY = 10; // small top offset

  const MenScale = 0.75;
  const MenTranslateX = (240 * (1 - MenScale)) / 2; // center horizontally
  const MenTranslateY = 10; // small top offset

  return (
    <svg
      viewBox={`0 0 240 ${vbHeight}`}
      xmlns="http://www.w3.org/2000/svg"
      className={className}
      style={{ overflow: "visible" }}
    >
      <SharedDefs />
      {category === "women" && (
        <g transform={`translate(${WomenTranslateX}, ${WomenTranslateY}) scale(${WomenScale})`}>
          <WomenBody />
          {renderOverlay?.(pos)}
        </g>
      )}
      {category === "men" && (
        <g transform={`translate(${MenTranslateX}, ${MenTranslateY}) scale(${MenScale})`}>
          <MenBody />
          {renderOverlay?.(pos)}
        </g>
      )}
      {category === "kids" && (
        <g transform={`translate(${kidsTranslateX}, ${kidsTranslateY}) scale(${kidsScale})`}>
          <KidsBody />
          {renderOverlay?.(pos)}
        </g>
      )}
    </svg>
  );
}

