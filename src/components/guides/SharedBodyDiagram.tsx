// ─── Composant principal (usage isolé par catégorie) ─────────────────────────

interface SharedBodyDiagramProps {
  category: Category;
  /** Rendu de l'overlay de mesures. Reçoit les positions du corps. */
  renderOverlay?: (pos: BodyPositions) => ReactNode;
  /** Hauteur du viewBox (optionnel, calculée automatiquement sinon). */
  viewBoxHeight?: number;
  className?: string;
}

export function SharedBodyDiagram({ category, renderOverlay, viewBoxHeight, className }: SharedBodyDiagramProps) {
  const pos = getPositions(category);
  const vbHeight = viewBoxHeight ?? (category === "kids" ? 345 : 340);

  // Scale kids to occupy ~75% of the space, matching women/men size
  const kidsScale = 1.0;
  const kidsTranslateX = 0;
  const kidsTranslateY = 0;

  // Scale up women & men for larger diagrams
  const womenScale = 1.55;
  const menScale = 1.35;

  // Women body SVG paths are centered around x≈293, need to shift to centerX=120
  // After scale: 293 * scale + tx = 120 → tx = 120 - 293 * scale
  const womenBodyOffsetX = 120 - 293 * womenScale;
  const womenBodyOffsetY = (vbHeight * (1 - womenScale)) / 2 + 60;

  // Men body SVG paths are centered around x≈428
  const menBodyOffsetX = 120 - 428 * menScale;
  const menBodyOffsetY = (vbHeight * (1 - menScale)) / 2 + 40;

  return (
    <svg
      viewBox={`0 0 240 ${vbHeight}`}
      xmlns="http://www.w3.org/2000/svg"
      className={className}
      style={{ overflow: "visible" }}
    >
      <SharedDefs />
      {category === "women" && (
        <>
          <g transform={`translate(${womenBodyOffsetX}, ${womenBodyOffsetY}) scale(${womenScale})`}>
            <WomenBody />
          </g>
          {renderOverlay?.(pos)}
        </>
      )}
      {category === "men" && (
        <>
          <g transform={`translate(${menBodyOffsetX}, ${menBodyOffsetY}) scale(${menScale})`}>
            <MenBody />
          </g>
          {renderOverlay?.(pos)}
        </>
      )}
      {category === "kids" && (
        <>
          <g transform={`translate(${kidsTranslateX}, ${kidsTranslateY}) scale(${kidsScale})`}>
            <KidsBody />
          </g>
          {renderOverlay?.(pos)}
        </>
      )}
    </svg>
  );
}

